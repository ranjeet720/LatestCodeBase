package ProgrammingPractice;

public class HelloWorldUsingLoop {
	
	public static void main(String[]args) {
		
		String s ="Hello World";
		int i =0;
		
		while(i<=10) {
			System.out.println(s);
			i++; //Increment operator can help to iterate till that limit but not lead to reach further 
			
		}
		
		
		
	}

}
